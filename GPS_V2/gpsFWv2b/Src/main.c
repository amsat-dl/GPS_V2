/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  fwdRxTx();	// tell the downconverter that we use ext. 10 MHz

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_LPUART1_UART_Init();
  /* USER CODE BEGIN 2 */
  readJumper();
  myUSARTrxInit();
  HAL_TIM_Base_Start_IT(&htim2);
  Si5328Ready();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	// init the GPS module every second until ACK was received
	if(ubx_ack == 0 && tick1ms > 1000)
	{
		tick1ms = 0;
		initGPS();
	}

	// send UART1 (GPS) to UART 2 (Downconverter)
	sendUART();

	// relock si5328 if GPS lock status changed
	if(gps_just_locked == 1)
	{
		gps_just_locked = 2;
		si5328_ical();
	}
  }
  /* USER CODE END 3 */
}

