/* USER CODE BEGIN 1 */
void delay_1ms(uint32_t del)
{
	del1ms = del;
	while(del1ms > 0);
	del1ms = del;
}

// called every ms in the Timer IRQ
void alive()
{
static int halfsec = 0;

	if(++halfsec > 500)
	{
		halfsec = 0;
		// here every 500ms
		if(ubx_ack == 1)
			HAL_GPIO_TogglePin(GPIOC, LED2_Pin);
		else
			HAL_GPIO_WritePin(GPIOC, LED2_Pin, GPIO_PIN_SET);
	}
}

